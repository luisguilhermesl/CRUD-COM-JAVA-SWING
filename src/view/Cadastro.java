    private void LimparFormulario() {
        nomeTxt.setText("");
        sobrenomeTxt.setText("");
        emailTxt.setText("");
        cidadeTxt.setText("");
        cpfTxt.setText("");
    }

    private void CarregarDadosUsuario() throws SQLException {
        UsuarioDAO uDao = new UsuarioDAO();
        List<Usuario> usuarios = uDao.getUsuarios();
        String[][] dados = new String[usuarios.size()][6];

        for (int i = 0; i < usuarios.size(); i++) {
            dados[i][0] = String.valueOf(usuarios.get(i).getId());
            dados[i][1] = usuarios.get(i).getNome();
            dados[i][2] = usuarios.get(i).getSobrenome();
            dados[i][3] = usuarios.get(i).getEmail();
            dados[i][4] = usuarios.get(i).getCidade();
            dados[i][5] = usuarios.get(i).getCpf();
        }
        String[] nomeColuna = {"Id", "Nome", "Sobrenome", "Email", "Cidade", "Cpf"};
        tabela.setModel(new javax.swing.table.DefaultTableModel(dados, nomeColuna));
    }

