    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        int linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada != -1) {
            IdUsuarioSelecionado = Integer.parseInt(tabela.getValueAt(linhaSelecionada, 0).toString());
            nomeTxt.setText(tabela.getValueAt(linhaSelecionada, 1).toString());
            sobrenomeTxt.setText(tabela.getValueAt(linhaSelecionada, 2).toString());
            emailTxt.setText(tabela.getValueAt(linhaSelecionada, 3).toString());
            cidadeTxt.setText(tabela.getValueAt(linhaSelecionada, 4).toString());
            cpfTxt.setText(tabela.getValueAt(linhaSelecionada, 5).toString());
        }
    }//GEN-LAST:event_tabelaMouseClicked

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String addNome = nomeTxt.getText();
        String addSobrenome = sobrenomeTxt.getText();
        String addEmail = emailTxt.getText();
        String addCidade = cidadeTxt.getText();
        String addCpf = cpfTxt.getText();

        if (addNome.isEmpty() || addSobrenome.isEmpty() || addEmail.isEmpty() || addCidade.isEmpty() || addCpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Usuario novoUsuario = new Usuario(0, addNome, addSobrenome, addEmail, addCidade, addCpf);
            UsuarioDAO uDao;

            try {
                uDao = new UsuarioDAO();
                int res = uDao.InserirUsuario(novoUsuario);
                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "Usuário criado com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                    this.LimparFormulario();
                    CarregarDadosUsuario();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ocorrido ao inserir o usuário", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        String addNome = nomeTxt.getText();
        String addSobrenome = sobrenomeTxt.getText();
        String addEmail = emailTxt.getText();
        String addCidade = cidadeTxt.getText();
        String addCpf = cpfTxt.getText();

        if (addNome.isEmpty() || addSobrenome.isEmpty() || addEmail.isEmpty() || addCidade.isEmpty() || addCpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            if (IdUsuarioSelecionado != -1) {
                Usuario AtualizarUsuario = new Usuario(IdUsuarioSelecionado, addNome, addSobrenome, addEmail, addCidade, addCpf);
                UsuarioDAO uDao;

                try {
                    uDao = new UsuarioDAO();
                    int res = uDao.updateUsuario(AtualizarUsuario);
                    if (res == 1) {
                        JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                        this.LimparFormulario();
                        CarregarDadosUsuario();
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao atualizar", "ERRO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if(IdUsuarioSelecionado != -1){
            int confirmacao = JOptionPane.showConfirmDialog(this, "Você tem certeza que quer deletar esse usuário?",
                    "Confirme a ação",JOptionPane.YES_NO_CANCEL_OPTION);
            if(confirmacao == JOptionPane.YES_OPTION){
                UsuarioDAO uDao;
                try{
                    uDao = new UsuarioDAO();
                    uDao.deleteUsuario(IdUsuarioSelecionado);
                    JOptionPane.showMessageDialog(this,"Usuário deletado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
                    this.LimparFormulario();
                    CarregarDadosUsuario();
                    IdUsuarioSelecionado = -1;
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(this, "Erro ao deletar","ERRO",JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this,"Nenhum usuário selecionado","ERRO",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

